function buildFont(
  glyphH: number,
  glyphs: Record<number, number[]>,
): Uint8Array {
  const data = new Uint8Array(95 * glyphH);
  for (const [code, glyph] of Object.entries(glyphs)) {
    const offset = (Number(code) - 32) * glyphH;
    for (let i = 0; i < glyphH; i++) data[offset + i] = glyph[i]!;
  }
  return data;
}

/** Draw a null-terminated string using a bitmap font. Only ON pixels are drawn (transparent bg). */
export function drawText(
  fb: Framebuffer,
  str: string,
  x: number,
  y: number,
  fontData: Uint8Array,
  glyphW: number,
  glyphH: number,
  advance: number,
): void {
  let cx = x;
  for (let i = 0; i < str.length; i++) {
    const code = str.charCodeAt(i);
    if (code >= 32 && code <= 126) {
      const offset = (code - 32) * glyphH;
      for (let row = 0; row < glyphH; row++) {
        const rowByte = fontData[offset + row]!;
        for (let col = 0; col < glyphW; col++) {
          if ((rowByte >>> (7 - col)) & 1) {
            setPixel(fb, cx + col, y + row, 1);
          }
        }
      }
    }
    cx += advance;
  }
}
