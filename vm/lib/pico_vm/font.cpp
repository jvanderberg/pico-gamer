void drawText(Framebuffer& fb, const char* str, int x, int y,
              const uint8_t* fontData, int glyphW, int glyphH, int advance) {
    int cx = x;
    for (int i = 0; str[i] != '\0'; i++) {
        int code = (unsigned char)str[i];
        if (code >= 32 && code <= 126) {
            int offset = (code - 32) * glyphH;
            for (int row = 0; row < glyphH; row++) {
                uint8_t rowByte = fontData[offset + row];
                for (int col = 0; col < glyphW; col++) {
                    if ((rowByte >> (7 - col)) & 1) {
                        setPixel(fb, cx + col, y + row, 1);
                    }
                }
            }
        }
        cx += advance;
    }
}
